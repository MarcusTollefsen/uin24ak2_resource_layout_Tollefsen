function openTab(evt, kategori) {
    const tabs = document.querySelectorAll(".tab");
    tabs.forEach(tab => tab.style.display = "none");
  
    const buttons = document.querySelectorAll(".knapp");
    buttons.forEach(button => button.classList.remove("active"));
  
    document.getElementById(kategori).style.display = "block";
    evt.currentTarget.classList.add("active");
  }
  

  // funksjon for å bytte innhold når man trykker på knappene
  // funksjon er hentet fra https://www.w3schools.com/howto/howto_js_tabs.asp


  function getTab() {
    const tabInnhold = document.querySelectorAll(".tab");
    
    resources.forEach(resource => {
      const category = resource.category;
      const text = resource.text;
      const sources = resource.sources;
  
      const tabContent = document.getElementById(category);
      const tabText = tabContent.querySelector(".tab-text");
      const tabSources = tabContent.querySelector(".tab-sources");
  
      tabText.textContent = text;
  
      sources.forEach(source => {
        const sourceLink = document.createElement("a");
        sourceLink.href = source.url;
        sourceLink.textContent = source.title;
        
        const listItem = document.createElement("li");
        listItem.appendChild(sourceLink);
        
        tabSources.appendChild(listItem);
      });
    });
  }
  
  getTab();
  